#include <unistd.h>

void	rush(int x, int y);

int		main(void)
{
	write(1, "\n--- 0 & 0 ---\n", 14);
	rush(0, 0);
	write(1, "\n--- -5 & -3 ---\n", 17);
	rush(-5, -3);
	write(1, "\n--- 5 & -3 ---\n", 17);
	rush(5, -3);
	write(1, "\n--- -5 & 3 ---\n", 17);
	rush(-5, 3);
	write(1, "\n--- 5 & 3 ---\n", 15);
	rush(5, 3);
	write(1, "\n--- 5 & 1 ---\n", 15);
	rush(5, 1);
	write(1, "\n--- 1 & 1 ---\n", 15);
	rush(1, 1);
	write(1, "\n--- 1 & 5 ---\n", 15);
	rush(1, 5);
	write(1, "\n--- 4 & 4 ---\n", 15);
	rush(4, 4);
	write(1, "\n--- 42 & 36 ---\n", 17);
	rush(42, 36);
	return (0);
}
